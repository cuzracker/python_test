# Python Data Structure
# list: [ ]
# tuple: ( )
# set: { }
# dict { : }

x = 100, 200
print(x)
print(type(x))
print()

fruits = ['apple', 'banana', 'pear']
print(fruits)
print(type(fruits))
print()

books = {}
print(books)
print(type(books))

books = {'apple', 'banana', 'pear'}
print(books)
print(type(books))

books = {'apple': 1000}
print(books)
print(type(books))
